// ==
let marks = 100;
console.log(marks == 200); // false

let  operators = '100';
console.log(operators == 100); // true - equal
console.log(operators != 100); // false - not equal

// ===
let play = '300';
console.log(play === 300) // false - equal
console.log(play !== 300) // true

// == equal টাইপ চেক করে না।।
// === equal টাইপ এবং ভেলু দুটই চেক করে।। === strict equality